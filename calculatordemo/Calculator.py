class Calculator:

    # 加法addition
    def add(self, a, b):
        return a + b

    # 减法subtraction
    def sub(self, a, b):
        return a - b

    # 乘法multiplication
    def mul(self, a, b):
        return a * b

    # 除法division
    def div(self, a, b):
        return a / b
